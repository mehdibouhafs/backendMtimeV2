	@Override
	public AppUser findUserByUsername(String username) {
		// TODO Auto-generated method stub
		return userRepository.findByUsername(username);
	}

	@Override
	public AppUser saveUser(AppUser appUser) {
		// TODO Auto-generated method stub
		return userRepository.save(appUser);
	}

	@Override
	public AppProfile saveProfile(AppProfile profile) {
		// TODO Autogenerated method stub
		return profileRepository.save(profile);
	}

	@Override
	public void addProfileToUser(String username, String profileName) {
		AppProfile profile = profileRepository.findByPrflName(profileName);
		AppUser user = userRepository.findByUsername(username);
		user.getProfiles().add(profile);
	}

	@Override
	public void addAuthorisationToProfile(String authorisation, String profileName) {
		// TODO Auto-generated method stub
		AppProfile profile = profileRepository.findByPrflName(profileName);
		Authorisation authorisation2 = authRepository.findByAuthName(authorisation);
		profile.getAuthorities().add(authorisation2);
	}

	@Override
	public Authorisation saveAuthorisation(String authorisationName) {
		// TODO Auto-generated method stub
		return authRepository.save(new Authorisation(null,authorisationName));
	}

	@Override
	public void addAuthorisationToUser(String authorisation, String username) {
		AppUser user = userRepository.findByUsername(username);
		Authorisation authorisation2 = authRepository.findByAuthName(authorisation);
		user.getAuthorities().add(authorisation2);
		
	}

	
	

